package derekahedron.invexpplus.util;

import net.minecraft.resources.ResourceLocation;

public class CompatUtil {

    public static ResourceLocation alexsCaves(String id) {
        return ResourceLocation.fromNamespaceAndPath("alexscaves", id);
    }

    public static ResourceLocation alexsMobs(String id) {
        return ResourceLocation.fromNamespaceAndPath("alexsmobs", id);
    }

    public static ResourceLocation farmersDelight(String id) {
        return ResourceLocation.fromNamespaceAndPath("farmersdelight", id);
    }
}
